<p class="indent">
	After realizing that I would be tight on money for the rest of the trip, I decided to try my luck
	at one of the ATM's. Same message every time: "insufficient funds" (of the ATM and not my credit
	card, I think). Even for small amounts like 1500CUP<sup><a href="#1">(1)</a></sup>, it did not
	work. An old man seeing my defeated face encourages me to try the bank next door. At the time I
	didn't know if it was some sort of scam and decided to get away. However, I turned back and saw
	that he tried using it as well. This man was not a local but an Australian tourist, that has been
	trying to use those ATM's for TWO WEEKS (without success)! After seeing that he tried, I also
	tried my luck... without success either, which is weird because a Cuban lady went between us and
	was able to get some cash, maybe there is a priority for Cubans, I don't know.
</p>

<p class="indent">
	He proceeded to give me a few tips that would come in handy during my trip in Vi&ntilde;ales. He
	showed me the store with the cheapest water, the restaurants that were good and affordable,
	churros street vendors, and a bakery that supposedly made good cakes. All these tips were provided
	to me during a walk through the city. I don't remember what lead to it, but we started a trip to a
	nearby cave: <i>La Cueva de la Vaca</i>. It wasn't the most accessible, but this man was walking
	fast and I had a bit of trouble to keep his pace. After maybe 30 minutes, we reach the entrance of
	the cave, guarded by a set of run-down stairs. We were however rewarded with a magnificent view of
	the valley.
</p>

