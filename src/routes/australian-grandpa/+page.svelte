<p class="indent">
	We begin to make our way down and follow a little path where we meet a pair of children. We ask
	them if the path continues to Vi&ntilde;ales, and they say that no, it doesn't. At this point, I
	was ready to turn the other way and start heading back from where we had arrived. My intrepid
	Australian Grandpa is not a quitter though, and we proceed on this path, that doesn't lead back to
	the city. Without retracing the whole journey, I am sure that not a lot of tourists visiting
	Vi&ntilde;ales have stepped where Antonio and I stepped. We had to crawl under a few sets of
	barbed wires, jump over a river, and continue in a field, with absolutely no path to guide us.
</p>

