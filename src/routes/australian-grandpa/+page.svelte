<p class="indent">
	We end up finding a road, the road that heads back to the city, we were still probably 3 or 4 km
	away. We finally reach the city after probably 2 hours in the suffocating heat. The rest of my
	time with him was spent in the bakery he told me about, where I took a green cake. I don't know if
	it had any taste, I could only taste the sugar, contrasted by a huge piece of salt in the
	meringue.
</p>

