<p class="indent">
	I thought we would turn and walk back to the city, but Antonio (the Australian Grandpa) had other
	plans. We continued deeper into the cave to reach another opening at the opposite side of the
	mountain. I wasn't the most reassured because Antonio told me that we might encounter bats, which,
	along with the dogs, ring the RABBIES bell for me. We, however, reach the opposite side and are
	again greeted with a marvelous view of the other side of the valley.
</p>

