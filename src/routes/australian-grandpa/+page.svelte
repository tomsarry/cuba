<p class="indent">
	We end up chatting about my stressful first day in La Havana, to which he ways that I shouldn't
	stress about such little things. He then explains that he survived a plane crash in the middle of
	the jungle when he was 22, my age. He continues by explaining how he was able to leave Afghanistan
	at around the same time, on a camel to Iran. I don't know if any of these stories are true, and I
	don't really care to be honest. I get anxious when I have to make any appointment and this man was
	traveling by himself, going off-road, and didn't mind getting completely lost, in the most serene
	way, while being 60 or 70 years old. Thank you Antonio, I'll try to stress less about my trip.
</p>

