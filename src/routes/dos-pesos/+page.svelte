<p class="indent">
	Having spent two nights in La Havana, I had a skewed perception of prices in Cuba. The hostel's
	manager (at la Havana) had told me to count approximately 1000 pesos per meal + cocktail. I was
	pleasantly surprised that this didn't apply everywhere in Cuba. For instance, I ordered a mojito
	in Vi&ntilde;ales for 100 CUP and a meal for 500 CUP. But this was nothing compared to Matanzas! I
	started off my morning with a Guava juice (10 CUP). Then, on my way to a viewpoint, I saw a little
	store that sold coffees for 5 CUP (not a fresh espresso but still). After getting back from the
	viewpoint, that was totally worth it by the way (not a single tourist in sight!), I tried my luck
	at a "Gastronomic Restaurant" (its name, I'm not making that up). This place was pretty popular
	among locals (high schoolers eating after class, workers) and was very cheap: I paid 125 CUP for
	my meal.
</p>

