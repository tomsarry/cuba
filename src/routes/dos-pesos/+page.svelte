<p class="indent">
	Finally, at the other side of one of the bridges, I took a sugarcane juice (fresh), with little
	chunks of ice (same of the first juice of the day), and it was also one of the best juices I've
	ever had. This judgement was certainly influenced by the fact that it was extremely hot, and that
	I was thirsty, but sill, a very good juice for 20 CUP.
</p>

