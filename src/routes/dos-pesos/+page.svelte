<p class="indent">
	It was fried rice, I think, and the lady that served me scooped my meal from what had been a large
	paint bucket. For that price, I can't really complain. Although the meal was quite filling, on my
	way to the other side of the city, a street vendor caught my eye. This cart was selling little
	fried corn batter balls (I think), for 2 pesos each. Two pesos. A USD is worth 180CUP today
	(approximately). I had a dozen and they were quite good, I have to admit.
</p>

