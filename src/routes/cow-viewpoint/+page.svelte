<p class="indent">
	After visiting by foot the valley for most of the afternoon with Antonio, I want to have a typical <i
		>siesta</i
	>, it was really nice to catch up on some sleep a bit. Antonio had told me about a great viewpoint
	close by, so I decided that it would be my goal of the evening. He advised me to come before 7pm,
	so I left my casa at around 6pm, with only a rough idea where to go. The instructions were that I
	should follow a road for a little bit, do a little zig-zag and I would arrive to a nice hotel
	overseeing the valley &mdash; a great spot to watch the sunset. Maybe 10 minutes after I started
	my journey, I ask a man on a wooden cart pulled by two cows where my destination is: Hostel
	Ermita. He tells me to just hop on, and that it is a kilometer away. Having had the "tourist"
	experience in La Havana (people are going to ask you for money for everything), and being
	currently pretty broke, I hesitated for a few seconds. In the end, I decided to hop on, I don't
	know when I'll be able to go on a cart driven by cows in the future. We chatted a bit on the way
	up, I told him where I was from, what I had visited today and he explained what he did in life:
	touristic horse rides in the morning <sup><a href="#1">(1)</a></sup> , and delivery service in the
	afternoon. At this moment, I realized that the cart was not empty as I originally thought, but it was
	carrying wood, to build a house. We exchanged names before I got off. His name is Adrian and I was
	very grateful for him that he carried me unconditionally. I thanked him, we said goodbye and he was
	off.
</p>

