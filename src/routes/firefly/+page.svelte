	<h2>Firefly</h2>

	<span>Matanza &middot; 04/05/2023 &middot; 9pm</span>
</div>

<p>
	Not much of a long story this time. I was chatting with Joan, a german tatoo artist (@prostatu_),
	mostly him telling me how much he disliked our host. The sun was way below the horizon towards the
	end of our talk. I was surprised to see a firefly. What surprised me was that the insect emitted a
	constant and bright light. The ones I had seen before (Vietnam, maybe Mauritius) were more
	flickery and (from what I recall) less bright. That's it, I saw a firefly, maybe two, but it made
	my night.
</p>
