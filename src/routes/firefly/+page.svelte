	<h2>Firefly</h2>

	<span>Matanzas &middot; 04/05/2023 &middot; 9pm</span>
</div>

<p class="indent">
	Not much of a long story this time. I was chatting with Joan, a german tatoo artist (<a
		href="https://www.instagram.com/prostatu_/"
		target="_blank"
		rel="noopener noreferrer nofollow">@prostatu_</a
	>), mostly him telling me how much he disliked our host. The sun was way below the horizon towards
	the end of our talk. I was surprised to see a firefly. What surprised me was that the insect
	emitted a constant and bright light. The ones I had seen before (Vietnam, maybe Mauritius) were
	more flickery and (from what I recall) less bright. That's it, I saw a firefly, maybe two, but it
	made my night.
</p>
