<p class="indent">
	We therefore enter, with Fadi (Toronto), Marine (Switzerland) and &mdash; (Spain). &mdash; told me
	her name 3 times and even spelled it a couple of times, without success for me to understand and
	even less to remember. The concept of this club is very nice and I had heard of something similar
	in Ottawa: A large museum they turn into a night club, sort of. What was so special about this
	place, explained a german girl from our group, is that it is one of the few places in Cuba where
	you can have politically engaged art pieces. Freedom of Speech is a problem here as I have been
	told. There was a main gallery displayed works from the collaboration of two Cuban artists: a
	painter and a photographer. Their work included a lot of ~modern / ~absurd pieces:
</p>

<ul>
	<li>man covered in white eggs in a bathtub with a cracked egg in his hand,</li>
	<li>man with an absurdly big necklace of sausages,</li>
	<li>naked man amongst pigs,</li>
</ul>

<p>
	to cite only a few. A second minor exposition was towards non-toxic masculinity and a dozen
	pictures of men wearing a flower on their ear were displayed. As it is a cause I deeply respect I
	got pictures taken of me with a flower as well, and one with Fadi.
</p>

