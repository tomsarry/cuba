<p class="indent">
	Tonight was the last night this "club" was opened in La Havana. It was closing for a month in
	order to prepare for the new season. Everyone at the hostel was talking about it so I thought I
	would check it out with them. It seemed famous, and indeed, after taking a minivan-bus with 11
	other people from the hostel, we arrive at this old factory with a huge line in front. A big
	poster was displayed at the entrance that confirmed to me the importance of this place.
</p>

